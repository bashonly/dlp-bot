def evaluate_requirements_txt(old_txt: str, new_txt: str) -> PythonUpdateResult:
    old_dict: dict[str, str] = {}
    new_dict: dict[str, str] = {}

    for txt, dct in [(old_txt, old_dict), (new_txt, new_dict)]:
        for line in txt.splitlines():
            if not line.strip() or line.startswith(('#', ' ')):
                continue
            dep = parse_dependency(line)
            dct.update({dep.name: dep.exact_version})

    return package_diff_dict(old_dict, new_dict)


def zipf_files_and_folders(zipf: zipfile.ZipFile, glob: str = '*') -> tuple[list[str], list[str]]:
    files = []
    folders = []

    path = zipfile.Path(zipf)
    for f in itertools.chain(path.glob(glob), path.rglob(glob)):
        if not f.is_file():
            continue
        # `f` is a `zipfile._path.Path` and it has an `at` attribute but mypy doesn't think so
        files.append(f.at)  # type: ignore[attr-defined]
        folder = f.parent.at.rstrip('/')  # type: ignore[attr-defined]
        if folder and folder not in folders:
            folders.append(folder)

    return files, folders


def makefile_variables(
    prefix: str,
    filetypes: list[str] | None = None,
    *,
    version: str | None = None,
    name: str | None = None,
    digest: str | None = None,
    data: bytes = b'',
    keys_only: bool = False,
) -> dict[str, str | None]:
    variables = {
        f'{prefix}_VERSION': version,
        f'{prefix}_WHEEL_NAME': name,
        f'{prefix}_WHEEL_HASH': digest,
    }
    for ft in filetypes or []:
        variables.update({
            f'{prefix}_{ft.upper()}_FOLDERS': None,
            f'{prefix}_{ft.upper()}_FILES': None,
        })

    if keys_only:
        return variables

    if not all(arg is not None for arg in (version, name, digest, not filetypes or data)):
        raise ValueError(
            f'makefile_variables requires version, name, digest, {"and data, " if filetypes else ""}OR keys_only=True'
        )

    if filetypes:
        with io.BytesIO(data) as buf, zipfile.ZipFile(buf) as zipf:
            for ft in filetypes:
                files, folders = zipf_files_and_folders(zipf, f'*.{ft.lower()}')
                variables[f'{prefix}_{ft.upper()}_FOLDERS'] = ' '.join(folders)
                variables[f'{prefix}_{ft.upper()}_FILES'] = ' '.join(files)

    return variables


def ejs_makefile_variables(**kwargs) -> dict[str, str | None]:
    return makefile_variables('EJS', filetypes=['PY', 'JS'], **kwargs)


def protobug_makefile_variables(**kwargs) -> dict[str, str | None]:
    return makefile_variables('PROTOBUG', filetypes=['PY'], **kwargs)


class YTDLPDependenciesUpdater(PythonDependenciesUpdater):
    def __init__(self, /, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._makefile_path = self.project_path / 'Makefile'
        self._requirements_path = self.project_path / 'bundle/requirements'

    def _pre_upgrade(
        self,
        /,
        updated_paths: set[pathlib.Path],
        *,
        upgrade_only: str | None,
        verify: bool,
    ):
        if upgrade_only is not None:
            updated_paths |= {
                'protobug': self.update_protobug,
                'yt-dlp-ejs': self.update_ejs,
            }[upgrade_only]()

        extras = get_extras(self.load_pyproject_toml(), resolve=False)

        # Remove pinned extras so they don't muck up the lockfile during generation/upgrade
        for pinned_extra_name in PINNED_EXTRAS:
            extras.pop(pinned_extra_name, None)

        # Write an intermediate pyproject.toml to use for generating lockfile and bundle requirements
        self.replace_pyproject_toml_table_and_write(
            table_name=EXTRAS_TABLE,
            table_dict=extras,
        )
        updated_paths.add(self.pyproject_path)

        return extras

    def _post_upgrade(
        self,
        extras: dict[str, list[str]],
        /,
        *,
        updated_paths: set[pathlib.Path],
        all_updates: PythonUpdateResult,
        env: dict[str, str] | None,
        upgrade_arg: str,
        upgrade_only: str | None,
        verify: bool,
    ):
        # Update Windows PyInstaller requirements; need to compare before & after .txt's for reporting
        if not upgrade_only:
            info = self.gh.get_latest_release('yt-dlp', 'Pyinstaller-Builds')
            for target_suffix, asset_tag in PYINSTALLER_BUILDS_TARGETS.items():
                asset_info = next(asset for asset in info['assets'] if asset_tag in asset['name'])

                requirements_path = self._requirements_path / REQS_OUTPUT_TMPL.format(target_suffix)
                if requirements_path.is_file():
                    old_requirements_txt = requirements_path.read_text()
                else:
                    old_requirements_txt = ''

                self.uv_pip_compile(
                    upgrade_arg,
                    input_line=f'pyinstaller @ {asset_info["browser_download_url"]}',
                    env=env,
                    output_file=requirements_path,
                )
                new_requirements_txt = requirements_path.read_text()
                if asset_info['digest'] not in new_requirements_txt:
                    raise VerificationError(
                        f'expected pyinstaller wheel hash {asset_info["digest"]} not found in {requirements_path}'
                    )

                updated_paths.add(requirements_path)
                diff_dict = evaluate_requirements_txt(old_requirements_txt, new_requirements_txt)
                if pyinstaller_diff := diff_dict.get('pyinstaller'):
                    # NB: this depends on 'pyinstaller[asset_tag]' keys in bot.knowledge.PYTHON_PACKAGES
                    all_updates.update({f'pyinstaller[{asset_tag}]': pyinstaller_diff})

        # Export bundle requirements; any updates to these are already recorded w/ uv.lock package diff
        for target_suffix, target in BUNDLE_TARGETS.items():
            requirements_path = self._requirements_path / REQS_OUTPUT_TMPL.format(target_suffix)
            self.uv_export(
                extras=target.extras,
                groups=target.groups,
                prune_packages=target.prune_packages,
                omit_packages=target.omit_packages,
                output_file=requirements_path,
            )
            updated_paths.add(requirements_path)

        # Export group requirements; any updates to these are already recorded w/ uv.lock package diff
        for group in EXPORT_GROUPS:
            requirements_path = self._requirements_path / REQS_OUTPUT_TMPL.format(group)
            self.uv_export(
                groups=[group],
                output_file=requirements_path,
            )
            updated_paths.add(requirements_path)

        # Compile requirements for single packages; need to compare before & after .txt's for reporting
        for package in COMPILE_PACKAGES:
            requirements_path = self._requirements_path / REQS_OUTPUT_TMPL.format(package)
            if requirements_path.is_file():
                old_requirements_txt = requirements_path.read_text()
            else:
                old_requirements_txt = ''

            self.uv_pip_compile(
                upgrade_arg,
                input_line=package,
                env=env,
                output_file=requirements_path,
            )
            updated_paths.add(requirements_path)

            new_requirements_txt = requirements_path.read_text()
            all_updates.update(evaluate_requirements_txt(old_requirements_txt, new_requirements_txt))

        # Generate new pinned extras; any updates to these are already recorded w/ uv.lock package diff
        for pinned_name, extra_name in PINNED_EXTRAS.items():
            extras[pinned_name] = self.uv_export(extras=[extra_name], bare=True)

        # Write the finalized pyproject.toml
        self.replace_pyproject_toml_table_and_write(
            table_name=EXTRAS_TABLE,
            table_dict=extras,
        )
        updated_paths.add(self.pyproject_path)

        # Generate/upgrade final lockfile that includes pinned extras
        self.uv('lock', upgrade_arg, env=env)
        updated_paths.add(self.lockfile_path)

    def update_ejs(self, /) -> set[pathlib.Path]:
        PACKAGE_NAME = 'yt-dlp-ejs'
        PREFIX = f'    "{PACKAGE_NAME}=='
        LIBRARY_NAME = PACKAGE_NAME.replace('-', '_')
        PACKAGE_PATH = self.project_path / 'yt_dlp/extractor/youtube/jsc/_builtin/vendor'

        current_version = None
        with self.pyproject_path.open() as file:
            for line in file:
                if not line.startswith(PREFIX):
                    continue
                current_version, _, _ = line.removeprefix(PREFIX).partition('"')

        if not current_version:
            raise ValueError(f'{PACKAGE_NAME} dependency line could not be found')

        makefile_info = ejs_makefile_variables(keys_only=True)
        prefixes = tuple(f'{key} = ' for key in makefile_info)
        with self._makefile_path.open() as file:
            for line in file:
                if not line.startswith(prefixes):
                    continue
                key, _, val = line.partition(' = ')
                makefile_info[key] = val.rstrip()

        info = self.gh.get_latest_release('yt-dlp', 'ejs')
        version = info['tag_name']
        if version == current_version:
            raise SuccessMessage(f'{PACKAGE_NAME} is up to date! ({version})')

        print(f'Updating {PACKAGE_NAME} from {current_version} to {version}', file=sys.stderr)
        updated_paths = set()
        hashes = []
        wheel_info = {}
        for asset in info['assets']:
            name = asset['name']
            digest = asset['digest']

            is_wheel = name.startswith(f'{LIBRARY_NAME}-') and name.endswith('.whl')
            if not is_wheel and name not in EJS_ASSETS:
                continue

            with request(asset['browser_download_url'], timeout=30) as resp:
                data = resp.read()

            # verify digest from github
            algo, _, expected = digest.partition(':')
            hexdigest = hashlib.new(algo, data).hexdigest()
            if hexdigest != expected:
                raise VerificationError(f'downloaded attest mismatch ({hexdigest!r} != {expected!r})')

            if is_wheel:
                wheel_info = ejs_makefile_variables(version=version, name=name, digest=digest, data=data)
                continue

            # calculate sha3-512 digest
            asset_hash = hashlib.sha3_512(data).hexdigest()
            hashes.append(f'    {name!r}: {asset_hash!r},')

            if EJS_ASSETS[name]:
                asset_path = PACKAGE_PATH / name
                asset_path.write_bytes(data)
                updated_paths.add(asset_path)

        hash_mapping = '\n'.join(hashes)
        if missing_assets := [asset_name for asset_name in EJS_ASSETS if asset_name not in hash_mapping]:
            raise ValueError(f'asset(s) not found in release: {", ".join(missing_assets)}')

        if missing_fields := [key for key in makefile_info if not wheel_info.get(key)]:
            raise ValueError(f'wheel info not found in release: {", ".join(missing_fields)}')

        vendored_info_path = PACKAGE_PATH / '_info.py'
        vendored_info_path.write_text(
            EJS_TEMPLATE.format(
                version=version,
                hash_mapping=hash_mapping,
            ),
        )
        updated_paths.add(vendored_info_path)

        content = self.pyproject_path.read_text()
        updated = content.replace(PREFIX + current_version, PREFIX + version)
        self.pyproject_path.write_text(updated)
        updated_paths.add(self.pyproject_path)

        makefile = self._makefile_path.read_text()
        for key in wheel_info:
            makefile = makefile.replace(f'{key} = {makefile_info[key]}', f'{key} = {wheel_info[key]}')
        self._makefile_path.write_text(makefile)
        updated_paths.add(self._makefile_path)

        return updated_paths

